package org.gusdb.gus.wdk.model;


public class TextAttribute {

    String name;
    String text;

    public TextAttribute() {}

    public void setName(String name) {
	this.name = name;
    }
    
    public String getName() {
	return name;
    }
    
    public void setText(String text) {
	this.text = text;
    }

    public String getText() {
	return text;
    }
    
}
